export function findPlantByName(name) {
  const lower = name.toLowerCase();
  return PLANT_DATABASE.find(p =>
    p.commonName.toLowerCase().includes(lower) ||
    p.turkishName.toLowerCase().includes(lower) ||
    p.scientificName.toLowerCase().includes(lower)
  );
}

export function getPlantsByCategory(category) {
  if (category === 'Hepsi') return PLANT_DATABASE;
  return PLANT_DATABASE.filter(p => p.category === category);
}

export const CATEGORIES = ['Hepsi', 'İç Mekan', 'Dış Mekan', 'Sukulent', 'Ot & Baharat', 'Tropikal', 'Meyve'];

export function getDaysUntilWater(plant) {
  if (!plant.lastWatered) return 0;
  const last = new Date(plant.lastWatered);
  const next = new Date(last.getTime() + plant.wateringDays * 24 * 60 * 60 * 1000);
  const now = new Date();
  const diff = Math.ceil((next - now) / (24 * 60 * 60 * 1000));
  return diff;
}

export function getWaterStatus(plant) {
  const days = getDaysUntilWater(plant);
  if (!plant.lastWatered || days <= 0) return { label: 'Susamış! 💧', color: '#D95F4B', urgent: true };
  if (days === 1) return { label: 'Yarın sula', color: '#E9A225', urgent: false };
  if (days <= 3) return { label: `${days} gün sonra`, color: '#E9A225', urgent: false };
  return { label: `${days} gün sonra`, color: '#2D6A4F', urgent: false };
}
